            dtm.addColumn("ENGINE NUMBER");
            dtm.addColumn("CHASISS NUMBER");
            dtm.addColumn("WEIGHT");
            dtm.addColumn("CAPACITY");
            dtm.addColumn("FUEL CAPACITY");
