    private void btnsave2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsave2ActionPerformed
        // TODO add your handling code here:
        String eng=txtengnum44.getText();
        String chass=txtchassnum4.getText();
        String weight=txtweight6.getText();
        String capa=cmbcapa4.getSelectedItem().toString();
        String fuel=cmbfuelcapa5.getSelectedItem().toString();

        String color= cmbcolor4.getSelectedItem().toString();
        String trance=txttrance4.getText();
        float  price =Float.parseFloat(txtprice.getText());
        
          if(eng.equals("")||chass.equals("")||weight.equals("")||capa.equals("")||trance.equals("")||fuel.equals("")||color.equals("")){
             
          JOptionPane.showMessageDialog(null,"Please fill Fealds");   
             
         }
          else{
        try
        {
            Statement st=dbcon.createDbConnection().createStatement();
            int row_count=st.executeUpdate("INSERT INTO honet VALUES('"+eng+"','"+chass+"','"+weight+"','"+capa+"','"+fuel+"','"+color+"','"+trance+"','"+price+"')");
            if(row_count>0)
            {
                JOptionPane.showMessageDialog(null, "Record Insert");
                avelable();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "ERROR");
            }

        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
          }
    }//GEN-LAST:event_btnsave2ActionPerformed

